package week7;

public class W2Practice3 {
//    Exercise Description
//
//Write a program that prints out $150k/year Job Offer Loading...
// 6 times using a while loop, followed by Offer from
// JPMorgan Chase Received! printed once.
public static void main(String[] args) {
     int num = 1;
     while (num <= 6){
         System.out.println("$150k/year Job Offer Loading...");
         num++;
     }
    System.out.println("Offer from JPMorgan Chase Received!");
}
}
