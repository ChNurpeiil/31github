package week7;

public class W3P2 {
//    Write a Java program that uses a while loop to
//    count from 1 to 10 and prints the numbers to the console.
//
//Input: There is no input required for this program.
//
//Output: The program should print the numbers 1
// through 10 to the console, each on a new line.
//
//Task: The task is to use a while loop to iterate through
// the numbers from 1 to 10 and print each number to the console.
public static void main(String[] args) {
    int num = 1;
    while (num <= 10){
        System.out.println(num);
        num++;
    }
}
}
